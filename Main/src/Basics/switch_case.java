package Basics;

public class switch_case {
    public static void main(String[] args) {
        int n=2;
        switch (n){
            case 1:
                System.out.println("1st option");
                break;
            case 2:
                System.out.println("2nd option");
                break;
            default:
                System.out.println("3rd option");
                break;
        }
    }

}
